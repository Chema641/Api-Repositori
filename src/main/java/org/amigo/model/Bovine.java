package org.amigo.model;

public class Bovine {
    private int idPet;
    private int rumenotomia;
    private int cesarea;
    private int reparacionProlapso;
    private int episiotomia;
    private int castracion;
    private int descornado;
    private int reparacionHernias;
    private int cirugiasPodales;


    public int getIdPet() { return idPet; }
    public void setIdPet(int idPet) { this.idPet = idPet; }

    public int getRumenotomia() { return rumenotomia; }
    public void setRumenotomia(int rumenotomia) { this.rumenotomia = rumenotomia; }

    public int getCesarea() { return cesarea; }
    public void setCesarea(int cesarea) { this.cesarea = cesarea; }

    public int getReparacionProlapso() { return reparacionProlapso; }
    public void setReparacionProlapso(int reparacionProlapso) { this.reparacionProlapso = reparacionProlapso; }

    public int getEpisiotomia() { return episiotomia; }
    public void setEpisiotomia(int episiotomia) { this.episiotomia = episiotomia; }

    public int getCastracion() { return castracion; }
    public void setCastracion(int castracion) { this.castracion = castracion; }

    public int getDescornado() { return descornado; }
    public void setDescornado(int descornado) { this.descornado = descornado; }

    public int getReparacionHernias() { return reparacionHernias; }
    public void setReparacionHernias(int reparacionHernias) { this.reparacionHernias = reparacionHernias; }

    public int getCirugiasPodales() { return cirugiasPodales; }
    public void setCirugiasPodales(int cirugiasPodales) { this.cirugiasPodales = cirugiasPodales; }

}
